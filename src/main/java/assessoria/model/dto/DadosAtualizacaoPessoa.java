package assessoria.model.dto;

public class DadosAtualizacaoPessoa {

    private String id;
    private String nome;
    private String email;
    private String cpf;
    private String telefone;
    private String novaSenha;

    public DadosAtualizacaoPessoa() {};

    public DadosAtualizacaoPessoa(String id, String nome, String email, String cpf, String telefone, String novaSenha) {
        this.id = id;
        this.nome = nome;
        this.email = email;
        this.cpf = cpf;
        this.telefone = telefone;
        this.novaSenha = novaSenha;
    }

    public String getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getNovaSenha() {
        return novaSenha;
    }

    public void setNovaSenha(String novaSenha) {
        this.novaSenha = novaSenha;
    }
}
